package cn.csbe.web.cms.order.bean;


/*
 * 商品评价
 * @author sxzhang
 */
public class OrderComment {
	
	private Integer id;
	private String commentContent;//评论内容
	private String sellerReplyContent;//回复
	private Integer userId;
	private Integer orderDetailsId;
	private Integer productId;
	private String commentImgJson;
	private String createDate;
	private String modifyDate;
	private Integer status;
	private String name;//product 商品表name
	private String titleImg;//对应商品标题图片
	private String userName;//对应用户名字	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getCommentContent() {
		return commentContent;
	}
	public void setCommentContent(String commentContent) {
		this.commentContent = commentContent;
	}
	public String getSellerReplyContent() {
		return sellerReplyContent;
	}
	public void setSellerReplyContent(String sellerReplyContent) {
		this.sellerReplyContent = sellerReplyContent;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public Integer getOrderDetailsId() {
		return orderDetailsId;
	}
	public void setOrderDetailsId(Integer orderDetailsId) {
		this.orderDetailsId = orderDetailsId;
	}
	public Integer getProductId() {
		return productId;
	}
	public void setProductId(Integer productId) {
		this.productId = productId;
	}
	public String getCommentImgJson() {
		return commentImgJson;
	}
	public void setCommentImgJson(String commentImgJson) {
		this.commentImgJson = commentImgJson;
	}
	public String getCreateDate() {
		return createDate;
	}
	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}
	public String getModifyDate() {
		return modifyDate;
	}
	public void setModifyDate(String modifyDate) {
		this.modifyDate = modifyDate;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTitleImg() {
		return titleImg;
	}
	public void setTitleImg(String titleImg) {
		this.titleImg = titleImg;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	
	
}
