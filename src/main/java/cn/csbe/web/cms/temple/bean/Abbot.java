package cn.csbe.web.cms.temple.bean;

import java.io.Serializable;

/**
 * 住持表
 * @author sxzhang
 *	
 */
public class Abbot implements Serializable {
	private Integer abbotId;//住持id
	private String abbotName;//住持名字
	private String createTime;
	private String modifyTime;
	private Integer status; //状态
	private Integer sortList; //排序
	public Integer getAbbotId() {
		return abbotId;
	}
	public void setAbbotId(Integer abbotId) {
		this.abbotId = abbotId;
	}
	public String getAbbotName() {
		return abbotName;
	}
	public void setAbbotName(String abbotName) {
		this.abbotName = abbotName;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getModifyTime() {
		return modifyTime;
	}
	public void setModifyTime(String modifyTime) {
		this.modifyTime = modifyTime;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public Integer getSortList() {
		return sortList;
	}
	public void setSortList(Integer sortList) {
		this.sortList = sortList;
	}
	
	
	
	
}
