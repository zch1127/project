package cn.csbe.web.cms.product.bean;
/*
 * @实体类 对应数据库的商品参数表
 * @author sxzhang
 */

public class ProductAttribute{
	private Integer id;
	private Integer productId;//对应的商品的id
	private String attrName;//参数标题
	private String attrValue;//规格
	private Integer enableStatus;
	private String createDate;
	private String modifyDate;
	@Override
	public String toString() {
		return "ProductAttribute [id=" + id + ", productId=" + productId
				+ ", attrName=" + attrName + ", attrValue=" + attrValue
				+ ", enableStatus=" + enableStatus + ", createDate="
				+ createDate + ", modifyDate=" + modifyDate + "]";
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getProductId() {
		return productId;
	}
	public void setProductId(Integer productId) {
		this.productId = productId;
	}
	public String getAttrName() {
		return attrName;
	}
	public void setAttrName(String attrName) {
		this.attrName = attrName;
	}
	public String getAttrValue() {
		return attrValue;
	}
	public void setAttrValue(String attrValue) {
		this.attrValue = attrValue;
	}
	public Integer getEnableStatus() {
		return enableStatus;
	}
	public void setEnableStatus(Integer enableStatus) {
		this.enableStatus = enableStatus;
	}
	public String getCreateDate() {
		return createDate;
	}
	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}
	public String getModifyDate() {
		return modifyDate;
	}
	public void setModifyDate(String modifyDate) {
		this.modifyDate = modifyDate;
	}
	
}
	
